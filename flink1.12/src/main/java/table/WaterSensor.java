package table;

import java.io.Serializable;

public class WaterSensor implements Serializable {
    public String id;
    public Long ts;
    public int vc;

    public WaterSensor(String id, Long ts, int vc) {
        this.id = id;
        this.ts = ts;
        this.vc = vc;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public Long getTs() {
        return ts;
    }

    public void setTs(Long ts) {
        this.ts = ts;
    }

    public int getVc() {
        return vc;
    }

    public void setVc(int vc) {
        this.vc = vc;
    }
}
